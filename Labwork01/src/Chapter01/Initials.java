package Chapter01;

public class Initials {
    public static void main(String[] args) {
        String[] phrases = {
            "**     **  **********  **        **",
            "**   **        **      ****      **",
            "** **          **      ** **     **",
            "***            **      **  **    **",
            "**             **      **   **   **",
            "***            **      **    **  **",
            "** **          **      **     ** **",
            "**    **       **      **      ****",
            "**     **      **      **       ***",
        };
        for (String phrase : phrases) {
            System.out.println(phrase);
        }
    }
}
